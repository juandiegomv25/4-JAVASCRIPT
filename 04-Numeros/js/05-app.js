//Incrementos o Decrementos

let puntaje = 5;
puntaje++;//5+1=6, puede funcionar para un juego de ptos como futbol
++puntaje;//6+1=7
puntaje--;//7-1=6
--puntaje;//6-1=5 

//Para aumentar + de 1pto, ejemplo basquet...
puntaje += 3;// el + debe ir adjunto al =, luego colocar el nro cualquiera
puntaje += 3;// si se duplica se va + al resultado ya obtenido
//Para restar + de 1pto...
puntaje -= 3;// el - debe ir adjunto al =, luego colocar el nro cualquiera
console.log(puntaje);




